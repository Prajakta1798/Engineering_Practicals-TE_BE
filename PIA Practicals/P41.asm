DATA SEGMENT WORD PUBLIC
M1 DB 10,13,"ENTER FIRST STRING: $"
M2 DB 10,13,"ENTER SECOND STRING: $"
M3 DB 10,13,"CONCATENATION STRING IS: $"
M4 DB 10,13,"ENTER YOUR CHOICE: $"

STR1 DB 20 DUP('#')
STR2 DB 20 DUP('#')
STR3 DB 20 DUP('#')
COUNT1 DB 0
COUNT2 DB 0
MENU DB 10,13,"MENU"
     DB 10,13,"1.CONCATENATION OF TWO STRING"
     DB 10,13,"2.COMPARISON "
     DB 10,13,"3.EXIT$"
DATA ENDS

PUBLIC STR1,STR2,STR3,COUNT1,COUNT2

CODE SEGMENT
ASSUME CS:CODE,DS:DATA,ES:DATA

START:

MOV AX,DATA
MOV DS,AX
MOV ES,AX

CALL READ

CALL MAIN

EXIT:MOV AH,4CH
INT 21H

MAIN PROC

MAIN1:MOV AH,09H
LEA DX,MENU
INT 21H

MOV AH,09H
LEA DX,M4
INT 21H

MOV AH,01H
INT 21H

CMP AL,31H
JE CASE1
CMP AL,32H
JE EXIT1

CASE1:MOV AH,09H
LEA DX,M3
INT 21H
CALL CONCATE
JMP MAIN1
EXIT1:MOV AH,4CH
INT 21H
RET
MAIN ENDP

READ PROC
MOV AH,09H
LEA DX,M1
INT 21H

LEA SI,STR1

L1:MOV AH,01H
INT 21H
MOV [SI],AL
INC SI
INC COUNT1
CMP AL,'#'
JNE L1
DEC COUNT1
MOV CL,COUNT1

MOV AH,09H
LEA DX,M2
INT 21H

LEA DI,STR2

L2:MOV AH,01H
INT 21H
MOV [DI],AL
INC DI
INC COUNT2
CMP AL,'#'
JNE L2
DEC COUNT2
MOV CL,COUNT2
RET
READ ENDP

CODE ENDS

END START