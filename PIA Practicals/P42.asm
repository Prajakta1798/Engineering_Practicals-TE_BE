DATA SEGMENT WORD PUBLIC
EXTRN STR1:BYTE
EXTRN STR2:BYTE
EXTRN STR3:BYTE
EXTRN COUNT1:BYTE
EXTRN COUNT2:BYTE

DATA ENDS

CODE1 SEGMENT WORD 'CODE'
ASSUME CS:CODE1,DS:DATA

START1:

CONCATE PROC FAR
PUBLIC CONCATE

LEA SI,STR1
LEA DI,STR1
MOV CL,[COUNT1]
K1:MOV AL,[SI]
MOV [DI],AL
MOV DL,AL
MOV AH,02H
INT 21H
INC SI
INC DI
DEC CL
JNZ K1


LEA SI,STR2
MOV CL,[COUNT2]
INC DI
K2:MOV AL,[SI]
MOV [DI],AL
MOV DL,AL
MOV AH,02H
INT 21H
INC SI
INC DI
DEC CL
JNZ K2
RET
CONCATE ENDP
CODE1 ENDS
END START1